// Um técnico em edificações percebe que necessita de 9 pedreiros para construir uma casa em 20 dias.
// Trabalhando com a mesma eficiência,
// quantos pedreiros são necessários para construir uma casa do mesmo tipo em 12 dias?

// Na matemática eu resolvi da seguinte forma: multipliquei o 9 * 20 = x * 12
// 12x = 180     x=180/12
// x = 15

let pedreiros2 = (9 * 20) / 12;

console.log(
  "Para construir uma casa em 12 dias, serão necessários " +
    pedreiros2 +
    " pedreiros."
);
